function bindEvents() {
  document.addEventListener("click", (e) => {
    const ans = e.target.closest("[data-answer]");
    if (ans) {
      const box = document.getElementById(ans.dataset.answer);
      box.classList.toggle("open");
      ans.textContent = box.classList.contains("open") ? "收起答案" : "点击显示答案";
    }
    const complete = e.target.closest("[data-complete]");
    if (complete) {
      const id = `complete-${complete.dataset.complete}`;
      const next = !isChecked(id);
      setChecked(id, next);
      complete.classList.toggle("done", next);
      complete.textContent = next ? "已完成本章" : "完成本章";
    }
    const start = e.target.closest("[data-start]");
    if (start) {
      setChecked(`start-${start.dataset.start}`, true);
      start.textContent = "学习中";
    }
  });
  document.addEventListener("change", (e) => {
    const c = e.target.closest("[data-check]");
    if (c) setChecked(c.dataset.check, c.checked);
  });
}

function updateProgress() {
  const boxes = [...document.querySelectorAll("[data-check]")];
  const chapterDone = DATA.priority.map(([name]) => isChecked(`complete-${cssId(name)}`)).filter(Boolean).length;
  const checked = boxes.filter(b => b.checked).length + chapterDone;
  const total = boxes.length + DATA.priority.length;
  const pct = total ? Math.round(checked / total * 100) : 0;
  $("#progressFill").style.width = `${pct}%`;
  $("#progressText").textContent = `${pct}%`;
}

