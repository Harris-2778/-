const $ = (sel) => document.querySelector(sel);
const el = (tag, cls, html) => {
  const node = document.createElement(tag);
  if (cls) node.className = cls;
  if (html !== undefined) node.innerHTML = html;
  return node;
};

function esc(s) {
  return String(s).replace(/[&<>"']/g, m => ({'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;'}[m]));
}

function storageKey(id) { return `anat-pass-${id}`; }
function isChecked(id) { return localStorage.getItem(storageKey(id)) === "1"; }
function setChecked(id, value) {
  localStorage.setItem(storageKey(id), value ? "1" : "0");
  updateProgress();
}

function renderToc() {
  const toc = $("#toc");
  const top = [
    ["home", "首页"],
    ["today", "今天怎么复习"],
    ["map", "考试内容地图"],
    ["visuals", "图像辅助"],
    ["exam", "总自测"],
    ["sprint", "2 小时冲刺"],
    ["memory", "背诵秘诀"],
    ["finalChecklist", "最后 checklist"],
  ];
  top.forEach(([id, label]) => toc.appendChild(el("a", "", label)).href = `#${id}`);
  const group = el("div", "toc-group");
  group.appendChild(el("div", "toc-label", "章节入口"));
  DATA.priority.forEach(([name]) => {
    const a = el("a", "", name);
    a.href = `#chapter-${cssId(name)}`;
    group.appendChild(a);
  });
  toc.appendChild(group);
}

function cssId(name) {
  return encodeURIComponent(name).replace(/%/g, "");
}

